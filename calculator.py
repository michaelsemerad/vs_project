"""
This is a program for simple mathematical operations with two numbers.

The operations are addition, subtraction, multiplication and division.

The input method is through the console.
"""


def add(x, y):
    """ Function adds two numbers.
    
    Args:
        - input - Input of the funcion are two numbers.
        
    Returs:
        - output - Output of the function is a addition of two numbers.
    """
    return x + y


def subtract(x, y):
    """ Function subtracts two numbers.
    
    Args:
        - input - Input of the funcion are two numbers.
        
    Returs:
        - output - Output of the function is a subtraction of two numbers.
    """
    return x - y


def multiply(x, y):
    """ Function multiplies two numbers.
    
    Args:
        - input - Input of the funcion are two numbers.
        
    Returs:
        - output - Output of the function is a multiplication of two numbers.
    """
    return x * y


def divide(x, y):
    """ Function divides two numbers.
    
    Args:
        - input - Input of the funcion are two numbers.
        
    Returs:
        - output - Output of the function is a division of two numbers.
    """
    return x / y
